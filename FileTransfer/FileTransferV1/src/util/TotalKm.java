package util;

public class TotalKm 
{
	float total_km_run ;
	float empty_run ;
	float loaded_run ;
	float cargo_loaded_run ;
	
	
	public float getTotal_km_run() {
		return total_km_run;
	}
	public void setTotal_km_run(float total_km_run) {
		this.total_km_run = total_km_run;
	}
	public float getEmpty_run() {
		return empty_run;
	}
	public void setEmpty_run(float empty_run) {
		this.empty_run = empty_run;
	}
	public float getLoaded_run() {
		return loaded_run;
	}
	public void setLoaded_run(float loaded_run) {
		this.loaded_run = loaded_run;
	}
	public float getCargo_loaded_run() {
		return cargo_loaded_run;
	}
	public void setCargo_loaded_run(float cargo_loaded_run) {
		this.cargo_loaded_run = cargo_loaded_run;
	}
	
}
